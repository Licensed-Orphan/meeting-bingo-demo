// Helper function to get category by ID
export const getCategoryById = (id: CategoryId): Category => {
  return categories[id];
};

// Helper function to get all category IDs
export const getCategoryIds = (): CategoryId[] => {
  return Object.keys(categories) as CategoryId[];
};

// Helper function to get random words from a category
export const getRandomWords = (categoryId: CategoryId, count: number): string[] => {
  const category = categories[categoryId];
  const shuffled = [...category.words].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

// Default export for convenience
export default categories;
